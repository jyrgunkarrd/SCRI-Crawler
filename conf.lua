function love.conf(t)
    t.identity = "scri-diablo"
    t.version = "11.5"
    t.console = false

    t.window.title = "SCRI Diablo"
    t.window.width = 1920
    t.window.height = 1080
    t.window.fullscreen = true
    t.window.fullscreentype = "exclusive"
    t.window.resizable = false
    t.window.vsync = 1
    t.window.msaa = 0
end
